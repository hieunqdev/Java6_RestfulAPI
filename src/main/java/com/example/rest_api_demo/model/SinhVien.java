package com.example.rest_api_demo.model;

public class SinhVien {
	private int id;
	private int maSv;
	private String ten;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	private int tuoi;
	public int getMaSv() {
		return maSv;
	}
	public void setMaSv(int maSv) {
		this.maSv = maSv;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public int getTuoi() {
		return tuoi;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	
	
}
